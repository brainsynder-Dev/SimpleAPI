    public static SoundMaker fromString(String soundName) {
        for (SoundMaker maker : SoundMaker.values()) {
            if (maker.v1_8_SoundName.equals(soundName)) {
                return maker;
            } else if (maker.v1_9_SoundName.equals(soundName)) {
                return maker;
            } else if (maker.v1_13_SoundName.equalsIgnoreCase(soundName)) {
                return maker;
            }else if (maker.name().equalsIgnoreCase(soundName)){
                return maker;
            }
        }
        return SoundMaker.UI_BUTTON_CLICK;
    }
    
    public String getSound() {
        if (ServerVersion.getVersion().getIntVersion() >= ServerVersion.v1_13_R1.getIntVersion()) return v1_13_SoundName;
        if (v1_8_SoundName.isEmpty() && v1_9_SoundName.isEmpty()) return name();
        if (ServerVersion.getVersion().getIntVersion() >= 19) return v1_9_SoundName;
        return v1_8_SoundName;
    }
    
