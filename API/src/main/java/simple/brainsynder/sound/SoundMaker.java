    public boolean canPlay () {
        if (version != null) {
            if (v1_9_SoundName.isEmpty() && v1_8_SoundName.isEmpty()) {
                // Will play if version is similar/greater then server
                return version.getIntVersion() >= ServerVersion.getVersion().getIntVersion();
            }else{
                // Will play if the version specified is not higher then server
                return version.getIntVersion() <= ServerVersion.getVersion().getIntVersion();
            }
        }

        return true;
    }
    
